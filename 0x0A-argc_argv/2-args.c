#include <stdio.h>
#include <stdlib.h>
#include "main.h"


/**
 * main - prints arguments received
 *
 * @argc: argument count
 * @argv: array of pointers
 *
 * Return: always 0.
 */


int main(int argc, char *argv[])
{

	int i;

	for (i = 1; i >= 1; i++);
	{
		printf("%d\n", argv[i]);
	}

	return (0);

}
